function happyHolidays() {
  return "Happy holidays!"
}

function happyHolidaysTo(person_name) {
  return `Happy holidays, ${person_name}!`
}

function happyHolidayTo(holiday, person_name) {
  return `Happy ${holiday}, ${person_name}!`
}

function holidayCountdown(holiday, days) {
  return `It's ${days} days until ${holiday}!`
}
